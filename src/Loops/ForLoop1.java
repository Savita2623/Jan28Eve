package Loops;

// for loop print number from 1 to 5
public class ForLoop1
{
	public static void main(String[] args) {
		
		//print num from 1 to 5
		
		//start num:1
		//end num:5
		//incr or decr: incr by 1
		
		//        1  6<=5   6
		for(int i=1; i<=5; i++)
		{
			System.out.println(i);
		}
		
		//1
		//2
		//3
		//4
		//5
	}

}
