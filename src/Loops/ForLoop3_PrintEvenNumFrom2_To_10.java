package Loops;

public class ForLoop3_PrintEvenNumFrom2_To_10 {
	
	public static void main(String[] args) {
		
		//print 2 to 10 even num
		
		//start num : 2
		// end num: 10
		//incr/ decr: incr by 2
		
		// 12<=1         //10+2=12
		for(int i=2; i<=10; i=i+2)
		{
			System.out.println(i);
		}
		
		//2
		//4
		//6
		//8
		//10
	}

}
